        char symbolStoresCharacter = '&';
        System.out.println("Переменная хранит символ & = " + symbolStoresCharacter);

        // Код ошибки
        int codeError = 404;
        System.out.println("Код ошибки = " + codeError);

        // Тип сообщения
        String messageType = "Error";
        System.out.println("Тип сообщения = " + messageType);

        // Число нулей
        int numberOfZeros = 3;
        System.out.println("Число нулей = " + numberOfZeros);

        // Уникальное число
        int uniqueNumber = 10;
        System.out.println("Уникальное число = " + uniqueNumber);

        // Случайное число
        int randomNumber = 9;
        System.out.println("Случайное число = " + randomNumber);

        // Математическое выражение
        int mathExpression = 5 * 5 * 5;
        System.out.println("Математическое выражение = " + mathExpression);

        // Счет в игре
        int gameScore = 0;
        System.out.println("Счет в игре = " + gameScore);

        // Максимальная длина
        int maxLength = 25;
        System.out.println("Максимальная длина = " + maxLength);

        // Пункт меню
        String menuItem = "Settings";
        System.out.println("Пункт меню = " + menuItem);

        // Стоимость кофе
        int costOfCoffee = 150;
        System.out.println("Стоимость кофе = " + costOfCoffee);

        // Начальная дата
        String startDate = "06.01.2025";
        System.out.println("Начальная дата = " + startDate);

        // Окончание диапазона
        int endRange = 55;
        System.out.println("Окончание диапазона = " + endRange);

