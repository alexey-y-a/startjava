        // Цифра
        int digit = 3;
        System.out.println("Цифра = " + digit);

        // Сумма чисел
        int sumOfTheNumbers = 7;
        System.out.println("Сумма чисел = " + sumOfTheNumbers);

        // Произведение цифр
        int productNumbers = 5 * 5;
        System.out.println("Произведение цифр = " + productNumbers);

